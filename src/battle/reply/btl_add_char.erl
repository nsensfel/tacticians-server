-spec generate
   (
      non_neg_integer(),
      btl_character:either(),
      non_neg_integer()
   )
   -> {list(any())}.
generate (IX, Character, PlayerIX) ->
   {
      [
         {<<"msg">>, <<"add_char">>},
         {<<"ix">>, IX},
         {<<"cha">>, btl_character:encode_for(PlayerIX, Character)}
      ]
   }.
