-spec generate (shr_player:type()) -> {list(any())}.
generate (Player) ->
   PID = shr_player:get_id(Player),
   SessionToken = shr_player:get_token(Player),

   {
      [
         {<<"msg">>, <<"sse">>},
         {<<"pid">>, PID},
         {<<"stk">>, SessionToken}
      ]
   }.
