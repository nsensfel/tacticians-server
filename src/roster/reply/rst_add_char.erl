-spec generate
   (
      non_neg_integer(),
      shr_character:unresolved()
   )
   -> {list(any())}.
generate (IX, Character) ->
   {
      [
         {<<"msg">>, <<"add_char">>},
         {<<"ix">>, IX},
         {<<"bas">>, shr_character:encode(Character)}
      ]
   }.
