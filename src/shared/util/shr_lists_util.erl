%%% Copy/pasted from the Erlang OTP's source code...
search (Pred, [Hd|Tail]) ->
   case Pred(Hd) of
      true -> {value, Hd};
      false -> search(Pred, Tail)
   end;
search (Pred, []) when is_function(Pred, 1) ->
   false.
