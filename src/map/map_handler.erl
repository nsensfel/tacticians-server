-spec start (pid()) -> 'ok'.
start (TimedCachesManagerPid) ->
   case shr_database:fetch(map_db, <<"0">>, admin) of
      {ok, _} -> ok;
      not_found ->
         shr_database:insert
         (
            map_db,
            <<"0">>,
            any,
            any,
            map_shim:generate_random_map()
         )
   end,
   shr_timed_caches_manager:new_cache(TimedCachesManagerPid, map_db, none),
   ok.
